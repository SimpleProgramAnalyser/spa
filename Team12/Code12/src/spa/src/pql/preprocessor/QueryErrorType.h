#ifndef SPA_PQL_PREPROCESSOR_QUERY_ERROR_TYPE_H
#define SPA_PQL_PREPROCESSOR_QUERY_ERROR_TYPE_H

// All the types of error a PQL query can have.
enum QueryErrorType { QuerySyntaxError, QuerySemanticsError, NoQueryErrorType };

#endif // SPA_PQL_PREPROCESSOR_QUERY_ERROR_TYPE_H
